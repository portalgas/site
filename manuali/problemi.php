	</li>
	<li>Cliccare sul tasto "Non spam"
		<p><a data-target="#modalImg" data-toggle="modal" href="" class="img_orig" title="clicca per ingrandire l'immagine"><img src="images/problemi-mail-gmail-tasto.png" class="img-responsive"></a></p>
	</li>
