<h2 id="non-ricevo-le-mail-di-portalgas-spam">Non ricevo le mail di PortAlGas: spam</h2>

<p>Controllare nella cartella "Spam" del proprio browser, il vostro Client di posta potrebbe averlo considerato un messaggio di Spam</p>
<p>Cosa fare per evitare che una mail di PortAlGas finisca nella cartella Spam: creare un apposto <strong>filtro</strong> che verrà impostato a tutte le mail che arrivano da PortAlGas</p>
<p>Impostando come filtro sull'oggetto della mail la parola "portalgas", si farà in modo che tutte le mail che contegono nell'oggetto della mail la parola "portalgas"</p>
<p>
<ul>
	<li>finiscano nella cartelle "portalgas"</li>
	<li>o gli venga associata l'etichetta "portalgas"</li>
</ul>
</p>
<p>dipenderà dal tipo di Client di posta che utilizzate</p>

